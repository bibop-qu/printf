#include "libft.h"

char			*ft_strdup(const char *s1)
{
	int			i;
	char		*cpy;

	i = ft_strlen(s1);
	cpy = (char*)malloc(sizeof(*cpy) * (i + 1));
	if (!cpy)
		return (0);
	i = 0;
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
