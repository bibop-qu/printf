#include "libft.h"

char				*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int		i;
	char				*new;

	i = 0;
	if (!s || !f)
		return (NULL);
	new = (char*)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!new)
		return (NULL);
	while (s[i])
	{
		new[i] = f(s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
